const mongoose = require('mongoose');

const UsersSchema = new mongoose.Schema({
    // columns for out document
    username: {
        type: String,
        required: true,
        unique: true,
    },
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
});

module.exports = mongoose.model('Messages', UsersSchema);